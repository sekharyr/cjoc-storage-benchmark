#!/bin/bash
# Pads vars/ with N trivial global-var files to simulate a large shared
# library's file-count footprint (STORAGE-OPTIONS.md §12.4). The controller
# has to check out real files that exist in the repo at a given git ref —
# this can't be faked at pipeline runtime, so padding has to be generated
# and committed ahead of time, then tagged as its own library version so
# Jenkinsfiles can reference @Library('<name>@<tag>') to sweep this axis.
#
# Padding files being unused (never called by name in any Jenkinsfile) still
# costs the controller the CHECKOUT — cloning/copying all of them into the
# library's local cache during retrieval — even though Jenkins' Groovy CPS
# engine won't spend anything extra to parse/compile a global var nothing
# calls. That's sufficient for this purpose: the thing being stressed is the
# controller writing many small files during library retrieval, not compute
# cost.
#
# Usage: ./scripts/generate-library-padding.sh [count]
#   ./scripts/generate-library-padding.sh 500
#   git add vars/benchPad*.groovy
#   git commit -m "Pad shared library to 500 files"
#   git tag lib-500files
#   git push --tags

set -euo pipefail

COUNT="${1:-500}"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
VARS_DIR="${SCRIPT_DIR}/../vars"

mkdir -p "${VARS_DIR}"

for i in $(seq 1 "${COUNT}"); do
    cat > "${VARS_DIR}/benchPad${i}.groovy" <<EOF
// Auto-generated by scripts/generate-library-padding.sh — padding file #${i}.
// Simulates shared-library file-count load; not called by any Jenkinsfile.
def call() {
    return ${i}
}
EOF
done

echo "Generated ${COUNT} padding files under ${VARS_DIR}"
echo "Next: git add vars/benchPad*.groovy && git commit -m 'Pad shared library to ${COUNT} files' && git tag lib-${COUNT}files"
